package Test_19June;

//Write a Java program that demonstrates proper variable declaration and initialization for
// different data types. **Requirements:** - Declare and initialize variables of different
// primitive data types (int, double, boolean, char)
// - Print the values of all variables - Show proper naming conventions

public class DataTypes1 {
    public static void main(String[] args) {
        int age = 18;
        double salary = 1234.50;
        boolean isGood = true;
        char grade = 'A';

        System.out.println("Age is " + age);
        System.out.println("Salary is " + salary);
        System.out.println("Are you good: " +isGood);
        System.out.println("Grade is " +grade);
    }
}
